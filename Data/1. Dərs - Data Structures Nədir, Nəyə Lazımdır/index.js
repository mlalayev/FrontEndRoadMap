//* 2. Data Structures: Məlumat Strukturları Nədir və Niyə Öyrənilməlidir?

//? **Məlumat Strukturu (Data Structure) Nədir?**
//? Məlumat strukturu, verilənlərin kompüter yaddaşında saxlanma, təşkil olunma və idarə edilmə üsuludur.
//? Proqramlaşdırma zamanı verilənlər təkcə yaddaşda saxlamaq üçün deyil, həm də sürətli və effektiv emal etmək üçün müəyyən metodlarla strukturlaşdırılır.
//? Məlumat strukturlarının əsas məqsədi, verilənlərin saxlanmasını və onlarla əməliyyat aparılmasını daha səmərəli hala gətirməkdir.

//? **Məlumat Strukturlarının Tətbiq Sahələri**
//? - **Verilənlər Bazaları:** Bütün müasir verilənlər bazaları müəyyən məlumat strukturlarından istifadə edərək verilənləri saxlayır və onlarla işləyir.
//? - **Axtarış Motorları:** Google və digər axtarış sistemləri mürəkkəb məlumat strukturlarından istifadə edərək verilənlərin sürətli indeksləşdirilməsini və axtarışını təmin edir.
//? - **Süni İntellekt və Maşın Öyrənməsi:** Böyük həcmli verilənləri idarə etmək və analitik hesablama aparmaq üçün məlumat strukturları əsasdır.
//? - **Oyun İnkişafı:** Oyun mühərrikləri, obyektlərin idarə edilməsi, fizika simulyasiyası və səhnələrin qurulması üçün məlumat strukturlarından istifadə edirlər.
//? - **Təhlükəsizlik və Kriptoqrafiya:** Məlumatların şifrələnməsi və autentifikasiya prosesləri müəyyən strukturlar vasitəsilə həyata keçirilir.
//? - **Veb Brauzerlər və DOM:** HTML səhifələrinin strukturunu təsvir edən DOM (Document Object Model) bir **ağac (tree) məlumat strukturu**dur və brauzerlərdə səhifənin qurulmasını idarə edir.
//? - **Əməliyyat Sistemləri:** İşlərin icra olunma sırasını idarə edən **queue (növbə) məlumat strukturu** istifadə olunur.
//? - **Geri və İrəli Butonları:** Brauzerlərdə istifadə olunan "geri" və "irəli" düymələri **stack (yığın) məlumat strukturu** vasitəsilə idarə edilir.

//? **Niyə Məlumat Strukturlarını Öyrənmək Lazımdır?**
//? - **Daha Effektiv Kod Yazmaq:** Məlumat strukturlarını düzgün seçmək proqramın sürətinə və yaddaş istifadəsinə birbaşa təsir edir.
//? - **Algoritm və Strukturlar Arasında Bağlantı:** Hər bir algoritm müəyyən bir məlumat strukturu üzərində işləyir və bunların düzgün kombinasiyası proqramlaşdırmada mühüm rol oynayır.
//? - **Kodun Daha Asan Təşkil Olunması:** Düzgün strukturlaşdırılmış məlumatlar proqramın oxunaqlılığını və idarə edilməsini asanlaşdırır.
//? - **Müsahibə və İş Prosesləri:** Proqramlaşdırma müsahibələrində və real iş mühitində məlumat strukturları ilə bağlı biliklər mütləq tələb olunur.
//? - **Mürəkkəb Problemlərin Həlli:** Çox böyük verilənlər üzərində işləyən proqramlarda düzgün məlumat strukturlarını seçmək uğurun əsas açarlarından biridir.

//? **Məlumat Strukturlarının Əsas Xüsusiyyətləri**
//? - **Saxlanma üsulu:** Verilənlər kompüter yaddaşında hansı formada saxlanır.
//? - **Giriş və Çıxış:** Verilənlərə necə daxil olmaq və onları necə dəyişmək mümkündür.
//? - **Əməliyyatlar:** Məlumat üzərində hansı əsas əməliyyatlar aparıla bilər (əlavə etmə, silmə, axtarış və s.).
//? - **Performans:** Verilənlərin emal edilməsi zamanı istifadə olunan vaxt və yaddaş resursları.

//? **Nəticə:**
//? Məlumat strukturlarını öyrənmək, proqramlaşdırmada daha effektiv və optimallaşdırılmış həllər qurmaq üçün vacibdir.
//? Real dünyadakı problemləri həll etmək və böyük verilənlərlə işləmək üçün məlumat strukturları əsas biliklərdən biridir.
