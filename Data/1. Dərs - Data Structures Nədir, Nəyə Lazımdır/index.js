//* 1. Dərs – Data Structure-lərə Giriş

//? **Data Structure nədir?**
//? Data Structure (Veri Strukturu) – məlumatların yaddaşda necə saxlandığını və təşkil olunduğunu ifadə edən quruluşlardır.
//? Yəni kompüterə deyirsən ki: “məlumatları necə düzüm, ki sonradan rahat istifadə edə bilim?”

//? Sadə dillə desək:
//? - Kitabxanadakı rəflər → hər kitabın yeri dəqiq bilinir
//? - Telefon kontaktları → ada görə və ya nömrəyə görə axtarırsan
//? - Kompüterdəki qovluqlar → məlumatlar qruplaşdırılıb saxlanır

//? **Data Structure-lər nəyə görə vacibdir?**
//? 1. **Sürət və Performans:** Məlumatı tez tapmaq, əlavə etmək və silmək
//? 2. **Təşkilatlılıq:** Kodun daha səliqəli və oxunaqlı olması
//? 3. **Problem Həll Etmə:** Ən optimal aləti seçməklə kodu daha ağıllı yazmaq

//? **Real həyat nümunələri:**
//? - Gözləmə növbəsi → Queue (FIFO – First In First Out)
//? - Tarayıcı “geri” düyməsi → Stack (LIFO – Last In First Out)
//? - Axtarış sistemləri → Tree və Hash Table
//? - Google Map-də yol tapma → Graph

//? **DS istifadə etmədən nə baş verir?**
//? - Kod çox uzanır, təkrarlanır və səmərəsiz olur
//? - Axtarış, sıralama, silmə əməliyyatları çox vaxt alır
//? - Proqramın performansı aşağı düşür

//? **DS + Algoritm birlikdə işləyir** – biri məlumatı saxlayır, digəri onu idarə edir.

//* --------------------------------------------
//* Sadə müqayisə üçün:
//* - Data Structure → Məlumatın "harada" və "necə" saxlanılması
//* - Algorithm → Məlumatla "nə" etmək istədiyin

//? Ona görə də birini bilmədən o biri tam işə yaramır.

