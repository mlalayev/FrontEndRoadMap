//* CLI (Command Line Interface) və GUI (Graphical User Interface), CMD və PowerShell Əmrləri

//? **CLI və GUI Nədir?**
//? CLI (Command Line Interface) və GUI (Graphical User Interface) istifadəçi ilə kompüter arasında fərqli əlaqə üsullarıdır. CLI, əmr satırı ilə işləmək, GUI isə qrafik pəncərələr və düymələr vasitəsilə əmrləri icra etməyi təklif edir.
//? Bu dərsdə **CMD (Command Prompt)** və **PowerShell**-də əsas əmrləri və GUI ilə müqayisə edilən əmrləri nəzərdən keçirəcəyik.

//? **CMD (Command Prompt)**
//? CMD, Windows əməliyyat sistemində istifadə olunan əsas əmr satırıdır. Bu mühitdə istifadəçilər müxtəlif əmrlər yazaraq faylları idarə edə, sistemlə əlaqə qura və əməliyyatları yerinə yetirə bilərlər.
//? **CMD** əmrləri terminal və ya PowerShell-də də işləyir, lakin bəzi fərqliliklər var.

//* **Əsas CMD Əmrləri**:

//? **mkdir (make directory)** - Yeni bir qovluq yaratmaq üçün istifadə edilir.
`mkdir newFolder`  //? Bu əmr "newFolder" adlı yeni bir qovluq yaradacaq.

//? **cd (change directory)** - Cari iş qovluğunu dəyişmək üçün istifadə edilir.
`cd path\to\directory`  //? Bu əmr sizi göstərilən qovluğa aparacaq.

//? **dir** - Cari qovluqdakı bütün faylları və qovluqları göstərir.
`dir`  //? Cari qovluqdakı bütün faylları və qovluqları göstərəcək.

//? **del** - Faylları silmək üçün istifadə edilir.
`del file.txt`  //? Bu əmr "file.txt" faylını siləcək.

//? **copy** - Fayl surətini yaratmaq üçün istifadə edilir.
`copy file.txt newFolder\file.txt`  //? Bu əmr "file.txt" faylını "newFolder" qovluğuna surət edəcək.

//? **cls (clear screen)** - Ekranı təmizləmək üçün istifadə edilir.
`cls`  //? Bu əmr ekranda olan bütün məzmunu siləcək və boş ekran göstərəcək.

//? **exit** - CMD-i bağlamaq üçün istifadə edilir.
`exit`  //? Bu əmr CMD-ni bağlayacaq.

