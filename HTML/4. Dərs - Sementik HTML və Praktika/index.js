//* 4. Gün - Semantik HTML və Praktika

//? Bu gün sementik HTML elementləri ilə tanış olacağıq. Sementik HTML, veb səhifələrdə daha yaxşı strukturlaşdırılmış, anlaşıqlı və SEO dostu kod yazmağa kömək edir.
//? Həmçinin praktika ilə bu elementlərin necə işlədiyini və necə istifadə edildiyini göstərəcəyik.

//* Sementik HTML Nədir?

//? Sementik HTML, məzmunu daha aydın şəkildə təmsil edən HTML elementlərindən istifadə etmək deməkdir.
//? Bu elementlər brauzerlər və inkişaf etdiricilər üçün məzmunu daha yaxşı başa düşülən edir. Sementik elementlər həmçinin SEO üçün faydalıdır.
//? Məsələn, `<article>`, `<section>`, `<header>`, `<footer>`, `<nav>`, `<main>` kimi elementlər sementik HTML-ə aiddir.


//* Sementik HTML Elementləri

//? 1. **`<header>`:** Saytın başlıq hissəsini təmsil edir. Adətən logo, naviqasiya və başlıq daxil olur.
//* `<header>` elementinin nümunəsi:
//* `<header>``
//* `  <h1>Saytın Başlığı</h1>`
//* `  <nav>`
//* `    <ul>`
//* `      <li><a href="#">Ana səhifə</a></li>`
//* `      <li><a href="#">Haqqımızda</a></li>`
//* `    </ul>`
//* `  </nav>`
//* `</header>`

//? 2. **`<footer>`:** Saytın alt hissəsini təmsil edir. Burada əlaqə məlumatları, sosial şəbəkə bağlantıları və s. yerləşir.
//* `<footer>` elementinin nümunəsi:
//* `<footer>`
//* `  <p>Əlaqə: example@example.com</p>`
//* `  <ul>`
//* `    <li><a href="#">Twitter</a></li>`
//* `    <li><a href="#">Facebook</a></li>`
//* `  </ul>`
//* `</footer>`

//? 3. **`<nav>`:** Saytın naviqasiyasını təmsil edir. Saytın müxtəlif bölmələrinə keçidlər buraya əlavə edilir.
//* `<nav>` elementinin nümunəsi:
//* `<nav>`
//* `  <ul>`
//* `    <li><a href="#">Ana səhifə</a></li>`
//* `    <li><a href="#">Xidmətlər</a></li>`
//* `    <li><a href="#">Əlaqə</a></li>`
//* `  </ul>`
//* `</nav>`

//? 4. **`<main>`:** Saytın əsas məzmununu təmsil edir. Bu element yalnız ana məzmunu əks etdirmək üçün istifadə olunur.
//* `<main>` elementinin nümunəsi:
//* `<main>`
//* `  <h2>Əsas Məzmun</h2>`
//* `  <p>Burada əsas məlumat yerləşir.</p>`
//* `</main>`

//? 5. **`<section>`:** Saytın müəyyən bir hissəsini təmsil edir, məsələn, xidmətlər, xəbərlər və ya məhsullar.
//* `<section>` elementinin nümunəsi:
//* `<section>`
//* `  <h2>Xidmətlər</h2>`
//* `  <p>Bizim təklif etdiyimiz xidmətlər burada veriləcək.</p>`
//* `</section>`

//? 6. **`<article>`:** Tam bir yazı və ya məqalə kimi müstəqil bir məzmunu təmsil edir. Adətən blog yazıları, xəbərlər və ya istifadəçi rəyləri üçün istifadə olunur.
//* `<article>` elementinin nümunəsi:
//* `<article>`
//* `  <h3>Blog Yazısı Başlığı</h3>`
//* `  <p>Yazının məzmunu burada olacaq...</p>`
//* `</article>`

//? 7. **`<aside>`:** Əsas məzmundan əlavə məlumatları təqdim etmək üçün istifadə olunur. Məsələn, tərəfli məqalələr, əlaqəli yazılar və ya reklamlar.
//* `<aside>` elementinin nümunəsi:
//* `<aside>`
//* `  <h3>Əlaqəli Yazılar</h3>`
//* `  <ul>`
//* `    <li><a href="#">Yazı 1</a></li>`
//* `    <li><a href="#">Yazı 2</a></li>`
//* `  </ul>`
//* `</aside>`

//* Praktika

//? Bu gün sementik HTML elementlərini praktika edəcəyik. Sadə bir veb səhifə hazırlayaq və müxtəlif sementik elementlərdən istifadə edək:

//* Saytın strukturunu belə qura bilərik:

//* `<header>` - Saytın başlığı
//  - `<nav>` - Naviqasiya
//  - `<main>` - Əsas məzmun
//    - `<section>` - Bir hissə
//    - `<article>` - Məqalə
//    - `<aside>` - Əlavə məlumat
//  - `<footer>` - Saytın alt hissəsi

