//* Frontend Developerlər Kimlərdir və Nə İş Görürlər?

//? Frontend developerlər, veb səhifələrinin və tətbiqlərinin istifadəçi qarşısında görünən hissələrini yaradan proqramçılardır. Onlar, istifadəçilərin qarşılıqlı əlaqəsini təmin etmək, vizual dizayn yaratmaq və tətbiqlərin işləməsini təmin etmək üçün HTML, CSS, JavaScript və digər texnologiyalardan istifadə edirlər.
//? Onların əsas məqsədi, istifadəçi təcrübəsini yaxşılaşdırmaq və funksionallığı təmin etməkdir. Frontend developer, həmçinin veb səhifələrinin hər cür cihazda düzgün işləməsini təmin edir (responsiv dizayn).


//* Frontend Developerlər Əlavə Framework Öyrənib Vəbdən Başqa Nə Düzəldə Bilərlər?

//? Frontend developer yalnız veb saytlar yaratmaqla kifayətlənmir. Onlar, əlavə olaraq aşağıdakı sahələrdə də fəaliyyət göstərə bilərlər:
//? 1. **Masaüstü tətbiqlər yaratmaq:** Electron.js istifadə edərək veb texnologiyaları ilə masaüstü tətbiqlər inkişaf etdirə bilərlər.
//? 2. **Mobil tətbiqlər yaratmaq:** React Native və ya Vue Native ilə mobil tətbiqlər hazırlaya bilərlər.
//? 3. **Backend proqramlaşdırma:** Node.js istifadə edərək server tərəfində tətbiqlər inkişaf etdirə bilərlər.
//? 4. **Full-Stack:** Frontend və backend texnologiyalarını birləşdirərək tam tətbiqlər inkişaf etdirə bilərlər.


//* Frontend Developerlər Hansı Proqramlaşdırma Dillərini Bilməlidirlər?

//? Frontend developerlər əsasən aşağıdakı proqramlaşdırma dillərini bilməlidirlər:
//? 1. **HTML (HyperText Markup Language):** Veb səhifələrinin strukturu üçün istifadə olunur.
//? 2. **CSS (Cascading Style Sheets):** Veb səhifələrinin görünüşünü və tərtibatını yaratmaq üçün istifadə olunur.
//? 3. **JavaScript:** Dinamik veb səhifələr yaratmaq, istifadəçi qarşılıqlı əlaqəsini təmin etmək və səhifə yeniləmələrini idarə etmək üçün istifadə olunur.
//? 4. **TypeScript:** JavaScript-in statik tipləmə əlavə edilmiş versiyasıdır. Daha etibarlı kod yazmağa imkan verir.

//? Əlavə olaraq, frontend developerlər framework-lər və kitabxanalar da öyrənməlidirlər:
//? 1. **React.js**
//? 2. **Vue.js**
//? 3. **Angular**

//* HTML-in AÇILIŞI NƏDİR?
//? HTML-in açılışı, HyperText Markup Language
//? Hipermətn İşarələmə(yazı) Dili

//* HTML Nə Üçün Lazımdır?
//? HTML (HyperText Markup Language), veb səhifələrinin əsas strukturunu təşkil etmək üçün istifadə olunan markup dilidir. HTML olmadan veb səhifələrinin əsas elementləri (başlıqlar, şəkillər, linklər və s.) təşkil oluna bilməz.
//? HTML, istifadəçilərə məlumatları göstərmək və onların qarşılıqlı əlaqəsini təmin etmək üçün əvəzsizdir.


//* KİM TƏRƏFİNDƏN YARANIB?
//? HTML, 1991-ci ildə **Tim Berners-Lee** tərəfindən yaradılıb. O, İnternetin inkişafında mühüm rol oynamış və World Wide Web (WWW) anlayışını yaratmışdır. HTML, o dövrdən etibarən veb dünyasının əsasını təşkil edir.


//* HTML OLMASA NƏ OLAR?
//? HTML olmadan veb səhifələri qurmaq mümkün olmazdı. Çünki HTML, veb səhifələrinin strukturunun təməlini təşkil edir. Bu olmadan veb səhifəsi yalnız şəkillər və mətn olmadan məna daşımazdı. HTML səhifə qurulması və məlumatların təqdimatı üçün vacibdir.

//* HTML ELEMENTLƏR NƏDİR?
//? HTML elementləri, veb səhifəsindəki məzmunun hissələrini müəyyən edən tərtibatlardır. Məsələn, başlıqlar, mətnlər, şəkillər və digər komponentlər HTML elementləri ilə təmsil olunur.
//? Əsas HTML elementləri:
//? - `<h1>` - başlıq
//? - `<p>` - paraqraf
//? - `<img>` - şəkil
//? - `<a>` - keçid (link)
//? - `<ul>` və `<ol>` - sırasız və sıralı siyahılar
//? - `<li>` - siyahı maddəsi

//* HTML Yeni Elementlər
//? HTML5 ilə bəzi yeni elementlər təqdim edilmişdir:
//? 1. `<section>` - Səhifənin bölmələri üçün istifadə olunur.
//? 2. `<article>` - Müəllif tərəfindən yazılmış məqalələri təqdim etmək üçün istifadə olunur.
//? 3. `<nav>` - Naviqasiya linksini təqdim etmək üçün istifadə olunur.
//? 4. `<footer>` - Səhifənin alt hissəsi üçün istifadə olunur.
//? 5. `<header>` - Səhifənin üst hissəsi üçün istifadə olunur.
//? 6. `<aside>` - Əsas məzmundan əlavə məlumatlar üçün istifadə olunur.


//* HTML Elementləri: `<head>`, `<body>`, `<h1>` - `<h6>`, `<p>`, `<img>`, `<ul>`, `<li>`, `<ol>`, `<span>`
//? - `<head>` - Metadata və digər əlaqəli elementləri ehtiva edir (başlıq, xarici fayllar və s.).
//? - `<body>` - Veb səhifənin görünən hissəsi. Bütün vizual məzmun burada yerləşir.
//? - `<h1> - <h6>` - Başlıqlar, `<h1>` ən vacib başlıqdır, `<h6>` isə ən kiçik başlıqdır.
//? - `<p>` - Paraqraf (mətn bloklarını təqdim etmək üçün istifadə olunur).
//? - `<img>` - Şəkil əlavə etmək üçün istifadə olunur.
//? - `<ul>` və `<ol>` - Sırasız və sıralı siyahılar üçün istifadə olunur.
//? - `<li>` - Siyahı maddələri üçün istifadə olunur.
//? - `<span>` - Inline elementlər üçün istifadə olunur, məzmunu qruplaşdırmaq və stil vermək üçün əlverişlidir.
